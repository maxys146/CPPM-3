#include <iostream>
#include "calc.h"

double calc_add(int a, int b)
{
    return a + b;
}
double calc_sub(int a, int b)
{
    return a - b;
}
double calc_mult(int a, int b)
{
    return a * b;
}
double calc_div(int a, int b)
{
    return a / b;
}
double calc_exp(int a, int b)
{
    return pow(a, b);
}